const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const manifestPath = path.join(root, 'builtins.json');
const generatedComment = 'Generated by tools/generate-builtins.js from builtins.json. Do not edit.';

function fail(message) {
  throw new Error(message);
}

function requireString(value, field) {
  if (typeof value !== 'string' || value.length === 0) {
    fail(`${field} must be a non-empty string`);
  }
}

function loadManifest() {
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  if (!Array.isArray(manifest.categories) || manifest.categories.length === 0) {
    fail('categories must be a non-empty array');
  }
  if (!Array.isArray(manifest.replWords)) fail('replWords must be an array');

  const categoryNames = new Set();
  const cArrays = new Set();
  const wordNames = new Set();
  const syntaxClasses = new Set(['builtin', 'control', 'operator']);

  for (const [categoryIndex, category] of manifest.categories.entries()) {
    const prefix = `categories[${categoryIndex}]`;
    requireString(category.name, `${prefix}.name`);
    requireString(category.cArray, `${prefix}.cArray`);
    if (!/^native_[a-z0-9_]+_words$/.test(category.cArray)) {
      fail(`${prefix}.cArray is not a native word-array identifier`);
    }
    if (categoryNames.has(category.name)) fail(`duplicate category: ${category.name}`);
    if (cArrays.has(category.cArray)) fail(`duplicate C array: ${category.cArray}`);
    categoryNames.add(category.name);
    cArrays.add(category.cArray);

    if (!Array.isArray(category.words) || category.words.length === 0) {
      fail(`${prefix}.words must be a non-empty array`);
    }
    for (const [wordIndex, word] of category.words.entries()) {
      const wordPrefix = `${prefix}.words[${wordIndex}]`;
      requireString(word.name, `${wordPrefix}.name`);
      requireString(word.cFunction, `${wordPrefix}.cFunction`);
      requireString(word.stackEffect, `${wordPrefix}.stackEffect`);
      requireString(word.description, `${wordPrefix}.description`);
      requireString(word.syntaxClass, `${wordPrefix}.syntaxClass`);
      if (word.syntax !== undefined && typeof word.syntax !== 'string') {
        fail(`${wordPrefix}.syntax must be a string`);
      }
      if (!/^tf_[a-z0-9_]+$/.test(word.cFunction)) {
        fail(`${wordPrefix}.cFunction is not a tf_ function identifier`);
      }
      if (!syntaxClasses.has(word.syntaxClass)) {
        fail(`${wordPrefix}.syntaxClass must be builtin, control, or operator`);
      }
      if (wordNames.has(word.name)) fail(`duplicate word: ${word.name}`);
      wordNames.add(word.name);
    }
  }

  for (const [wordIndex, word] of manifest.replWords.entries()) {
    const prefix = `replWords[${wordIndex}]`;
    requireString(word.name, `${prefix}.name`);
    requireString(word.cFunction, `${prefix}.cFunction`);
    requireString(word.stackEffect, `${prefix}.stackEffect`);
    requireString(word.description, `${prefix}.description`);
    if (!/^[a-z][a-z0-9_]*$/.test(word.cFunction)) {
      fail(`${prefix}.cFunction is not a static C function identifier`);
    }
    if (wordNames.has(word.name)) fail(`duplicate word: ${word.name}`);
    wordNames.add(word.name);
  }

  return manifest;
}

function nativeWords(manifest) {
  return manifest.categories.flatMap((category) => category.words);
}

function cString(value) {
  return JSON.stringify(value);
}

function renderCRegistry(manifest) {
  const lines = [
    `/* ${generatedComment} */`,
    '/* Categories organize help output by language concept, not C module. */',
  ];
  for (const category of manifest.categories) {
    lines.push(`static const tf_builtin_word ${category.cArray}[] = {`);
    for (const word of category.words) {
      lines.push(`    {${cString(word.name)}, ${word.cFunction}},`);
    }
    lines.push('    {NULL, NULL},', '};', '');
  }
  lines.push('static const tf_builtin_group native_builtin_groups[] = {');
  for (const category of manifest.categories) {
    lines.push(`    {${cString(category.name)}, ${category.cArray}},`);
  }
  lines.push('};', '');
  return lines.join('\n');
}

function renderReplRegistry(manifest) {
  const lines = [
    `/* ${generatedComment} */`,
    'static const tf_builtin_word repl_words[] = {',
  ];
  for (const word of manifest.replWords) {
    lines.push(`    {${cString(word.name)}, ${word.cFunction}},`);
  }
  lines.push('    {NULL, NULL},', '};', '');
  return lines.join('\n');
}

function renderRuntimeDocs(manifest) {
  const words = [...nativeWords(manifest), ...manifest.replWords];
  const lines = [
    '#include "tf_docs.h"',
    '#include <string.h>',
    '',
    `/* ${generatedComment} */`,
    'static const tf_doc_entry builtin_docs[] = {',
  ];
  for (const word of words) {
    lines.push(
      `    {${cString(word.name)}, ${cString(word.stackEffect)}, ${cString(word.syntax || '')}, ${cString(word.description)}},`
    );
  }
  lines.push(
    '};',
    '',
    'const tf_doc_entry *tf_doc_entries(size_t *count) {',
    '    if (count) *count = sizeof(builtin_docs) / sizeof(builtin_docs[0]);',
    '    return builtin_docs;',
    '}',
    '',
    'const tf_doc_entry *tf_doc_lookup(const char *name) {',
    '    if (!name) return NULL;',
    '    for (size_t i = 0; i < sizeof(builtin_docs) / sizeof(builtin_docs[0]); i++) {',
    '        if (strcmp(builtin_docs[i].name, name) == 0) return &builtin_docs[i];',
    '    }',
    '    return NULL;',
    '}',
    ''
  );
  return lines.join('\n');
}

function renderLspDocs(manifest) {
  const lines = [
    `// ${generatedComment}`,
    'package analysis',
    '',
    'var builtinDocs = map[string]builtinDoc{',
  ];
  for (const word of nativeWords(manifest)) {
    lines.push(`\t${JSON.stringify(word.name)}: {`);
    lines.push(`\t\tStackEffect: ${JSON.stringify(word.stackEffect)},`);
    if (word.syntax) lines.push(`\t\tSyntax:      ${JSON.stringify(word.syntax)},`);
    lines.push(`\t\tDescription: ${JSON.stringify(word.description)},`);
    lines.push('\t},');
  }
  lines.push('}', '');
  return lines.join('\n');
}

function renderTreeSitterWords(manifest) {
  const words = nativeWords(manifest);
  const renderArray = (name, syntaxClass) => {
    const lines = [`export const ${name} = [`];
    for (const word of words.filter((entry) => entry.syntaxClass === syntaxClass)) {
      lines.push(`  ${JSON.stringify(word.name)},`);
    }
    lines.push('];', '');
    return lines;
  };
  return [
    `// ${generatedComment}`,
    ...renderArray('controlWords', 'control'),
    ...renderArray('operatorWords', 'operator'),
    ...renderArray('builtinWords', 'builtin'),
  ].join('\n');
}

function regexEscape(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function renderVsCodeGrammar(manifest) {
  const words = nativeWords(manifest);
  const nameSegment = '[a-zA-Z0-9_+\\-*%<>=!?]+';
  const callSegment = '[a-zA-Z_+\\-*%<>=!?][a-zA-Z0-9_+\\-*%<>=!?]*';
  const qualifiedName = `(?:\\.(?:s|S)?|${nameSegment}(?:\\.${nameSegment})*)`;
  const qualifiedCall = `${callSegment}(?:\\.${callSegment})+`;
  const constantWords = ['pi', 'e', 'tau', 'inf', 'nan'];
  const byClass = (syntaxClass) =>
    words.filter((word) => word.syntaxClass === syntaxClass).map((word) => regexEscape(word.name));
  const builtinPattern = `(?<=\\s|^)(${byClass('builtin').filter((word) => word !== 'def' && !constantWords.includes(word)).join('|')})(?=\\s|$)`;
  const controlPattern = `\\b(${byClass('control').join('|')})\\b`;
  const operatorPattern = `(?<=\\s|^)(${byClass('operator').join('|')})(?=\\s|$)`;
  const grammar = {
    $schema: 'https://raw.githubusercontent.com/martinring/tmlanguage/master/tmlanguage.json',
    name: 'Toy',
    scopeName: 'source.toy',
    patterns: [
      { include: '#comments' },
      { include: '#strings' },
      { include: '#variables' },
      { include: '#functions' },
      { include: '#keywords' },
      { include: '#numbers' },
      { include: '#words' },
    ],
    repository: {
      comments: {
        patterns: [
          { name: 'comment.block.toy', begin: '/\\*', end: '\\*/' },
          { name: 'comment.line.backslash.toy', match: '\\\\.*$' },
        ],
      },
      strings: {
        patterns: [{
          name: 'string.quoted.double.toy',
          begin: '"',
          end: '"',
          patterns: [
            { name: 'constant.character.escape.toy', match: '\\\\(?:[nrt"\\\\]|x[0-9a-fA-F]{2})' },
            { name: 'invalid.illegal.escape.toy', match: '\\\\.' },
          ],
        }],
      },
      variables: {
        patterns: [
          { name: 'variable.parameter.toy', match: `\\$${nameSegment}` },
          {
            name: 'meta.binding-list.toy',
            begin: '\\|',
            beginCaptures: { 0: { name: 'punctuation.bracket.toy' } },
            end: '\\|',
            endCaptures: { 0: { name: 'punctuation.bracket.toy' } },
            patterns: [{ name: 'variable.parameter.toy', match: callSegment }],
          },
        ],
      },
      functions: {
        patterns: [
          { name: 'entity.name.function.toy', match: `'(?:/|${qualifiedName})(?=\\s*\\[)` },
          { name: 'variable.other.toy', match: `'(?:/|${qualifiedName})` },
          { name: 'support.function.builtin.toy', match: builtinPattern },
        ],
      },
      keywords: {
        patterns: [
          { name: 'keyword.control.toy', match: controlPattern },
          { name: 'storage.type.toy', match: '(?<=\\s|^)(def|\\|)(?=\\s|$)' },
          { name: 'keyword.operator.toy', match: operatorPattern },
        ],
      },
      numbers: {
        patterns: [{
          name: 'constant.numeric.toy',
          match: '(?<![a-zA-Z0-9_])[-+]?\\d+(?:\\.\\d*)?(?:[eE][-+]?\\d+)?(?![a-zA-Z0-9_.])',
        }],
      },
      words: {
        patterns: [
          { name: 'constant.language.toy', match: `\\b(true|false|${constantWords.join('|')})\\b` },
          { name: 'variable.other.toy', match: qualifiedCall },
          { name: 'variable.other.toy', match: `(?<![a-zA-Z0-9_+\\-*%<>=!?.])${callSegment}(?![a-zA-Z0-9_+\\-*%<>=!?.])` },
        ],
      },
    },
  };
  return `${JSON.stringify(grammar, null, 4)}\n`;
}

function renderReadme(manifest, current) {
  const begin = '<!-- BEGIN GENERATED BUILTIN TABLE -->';
  const end = '<!-- END GENERATED BUILTIN TABLE -->';
  const categoryWidth = Math.max('Category'.length, ...manifest.categories.map((category) => category.name.length));
  const table = [
    `| ${'Category'.padEnd(categoryWidth)} | Words |`,
    `| ${'-'.repeat(categoryWidth)} | ----- |`,
    ...manifest.categories.map((category) =>
      `| ${category.name.padEnd(categoryWidth)} | ${category.words.map((word) => `\`${word.name}\``).join(', ')} |`
    ),
  ].join('\n');
  const pattern = new RegExp(`${begin}[\\s\\S]*?${end}`);
  if (!pattern.test(current)) fail('README.md is missing generated builtin table markers');
  return current.replace(pattern, `${begin}\n${table}\n${end}`);
}

function main() {
  const args = process.argv.slice(2);
  if (args.length > 1 || (args.length === 1 && args[0] !== '--check')) {
    console.error('usage: node tools/generate-builtins.js [--check]');
    process.exitCode = 2;
    return;
  }
  const check = args[0] === '--check';
  const manifest = loadManifest();
  const readmePath = path.join(root, 'README.md');
  const targets = new Map([
    ['src/tf_builtins.inc', renderCRegistry(manifest)],
    ['src/tf_repl_builtins.inc', renderReplRegistry(manifest)],
    ['src/tf_docs.c', renderRuntimeDocs(manifest)],
    ['tools/toy-lsp/internal/analysis/builtin_docs_generated.go', renderLspDocs(manifest)],
    ['tools/tree-sitter-toy/builtin-words.js', renderTreeSitterWords(manifest)],
    ['tools/vscode-toy/syntaxes/toy.tmLanguage.json', renderVsCodeGrammar(manifest)],
    ['README.md', renderReadme(manifest, fs.readFileSync(readmePath, 'utf8'))],
  ]);

  const stale = [];
  for (const [relativePath, expected] of targets) {
    const targetPath = path.join(root, relativePath);
    const actual = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : null;
    if (actual === expected) continue;
    stale.push(relativePath);
    if (!check) {
      fs.mkdirSync(path.dirname(targetPath), { recursive: true });
      fs.writeFileSync(targetPath, expected);
      console.log(`wrote ${relativePath}`);
    }
  }

  if (check && stale.length > 0) {
    for (const relativePath of stale) console.error(`out of date: ${relativePath}`);
    process.exitCode = 1;
  } else if (stale.length === 0) {
    console.log('builtin generated files are up to date');
  }
}

try {
  main();
} catch (error) {
  console.error(`error: ${error.message}`);
  process.exitCode = 1;
}
