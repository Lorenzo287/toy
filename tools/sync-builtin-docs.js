const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const lspDocsPath = path.join(
  root,
  'tools',
  'toyforth-lsp',
  'internal',
  'analysis',
  'builtins.go'
);
const outPath = path.join(root, 'src', 'tf_docs.c');

function unescapeGoString(s) {
  return s
    .replace(/\\"/g, '"')
    .replace(/\\n/g, '\n')
    .replace(/\\t/g, '\t')
    .replace(/\\\\/g, '\\');
}

function cString(s) {
  return s
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
    .replace(/\t/g, '\\t');
}

const source = fs.readFileSync(lspDocsPath, 'utf8');
const entryRe = /^\s*"((?:\\.|[^"])*)":\s*\{([^}]*)\},/gm;
const fieldRe = /\b(StackEffect|Syntax|Description):\s*"((?:\\.|[^"])*)"/g;
const entries = [];
let match;

while ((match = entryRe.exec(source)) !== null) {
  const fields = {};
  let fieldMatch;
  while ((fieldMatch = fieldRe.exec(match[2])) !== null) {
    fields[fieldMatch[1]] = unescapeGoString(fieldMatch[2]);
  }

  const name = unescapeGoString(match[1]);
  entries.push({
    name,
    stackEffect: fields.StackEffect || '',
    syntax: fields.Syntax || '',
    description: fields.Description || '',
  });
}

entries.push(
  {
    name: 'hints',
    stackEffect: '--',
    syntax: '',
    description: 'Toggle REPL inline stack-effect hints.',
  },
  {
    name: 'trace',
    stackEffect: '--',
    syntax: '',
    description: 'Toggle REPL stack display after successful input.',
  }
);

const rows = entries.map(
  (entry) =>
    `    {"${cString(entry.name)}", "${cString(entry.stackEffect)}", "${cString(entry.syntax)}", "${cString(entry.description)}"},`
);

const output = `#include "tf_docs.h"
#include <string.h>

/* Generated by tools/sync-builtin-docs.js from the LSP hover table. */
static const tf_doc_entry builtin_docs[] = {
${rows.join('\n')}
};

const tf_doc_entry *tf_doc_entries(size_t *count) {
    if (count) *count = sizeof(builtin_docs) / sizeof(builtin_docs[0]);
    return builtin_docs;
}

const tf_doc_entry *tf_doc_lookup(const char *name) {
    if (!name) return NULL;
    for (size_t i = 0; i < sizeof(builtin_docs) / sizeof(builtin_docs[0]); i++) {
        if (strcmp(builtin_docs[i].name, name) == 0) return &builtin_docs[i];
    }
    return NULL;
}
`;

fs.writeFileSync(outPath, output);
console.log(`wrote ${entries.length} doc entries to ${path.relative(root, outPath)}`);
